    private void btnBuscarRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarRActionPerformed

         String placa = txtPlacaR.getText().trim();
    if (placa.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ingresa una placa.");
        return;
    }

//////////////////// Usar la √∫ltima salida registrada
    proyectorparqueo.model.ReciboSalida r = proyectorparqueo.model.DatosApp.ultimaSalidaDe(placa);

    if (r == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "No hay registro previo de salida para esta placa.");
        txtAreaR.setText("");
        btnReingresar.setEnabled(false);
        return;
    }

    proyectorparqueo.model.vehiculo v = r.getVehiculo();
    java.time.LocalDateTime ahora = java.time.LocalDateTime.now();
    long minutos = java.time.Duration.between(r.getHoraSalida(), ahora).toMinutes();

/////////////////////// Normalizar plan
    String plan = (v.getTipoPlan() == null) ? "" : v.getTipoPlan().toUpperCase();

    boolean puedeReingresarGratis = false;
    String mensaje;

    if (plan.contains("FLAT")) {
        if (minutos <= 120) {
            puedeReingresarGratis = true;
            mensaje = " PLAN (FLAT): reingreso GRATUITO (dentro de las 2 horas).";
        } else {
            mensaje = "Ô∏è PLAN (FLAT): han pasado m√°s de 2 horas.\n"
                    + "El plan se considera vencido, se cobrar√° nuevamente en un nuevo ingreso.";
        }
    } else {
        mensaje = "üî∏ PLAN VARIABLE: se cobrar√° normalmente al salir.";
    }

    txtAreaR.setText(String.format(
        "PLACA: %s%n" +
        "PROPIETARIO: %s%n" +
        "TIPO: %s%n" +
        "PLAN: %s%n" +
        "ROL: %s%n" +
        "AREA: %s%n" +
        "HORA SALIDA: %s%n" +
        "TIEMPO DESDE SALIDA: %d min%n%n" +
        "%s",
        v.getPlaca(),
        v.getPropietario(),
        v.getTipoVehiculo(),
        v.getTipoPlan(),
        v.getRol(),
        v.getArea(),
        r.getHoraSalida(),
        minutos,
        mensaje
    ));

////////////////////// Solo habilitar el bot√≥n si puede reingresar GRATIS con FLAT
    btnReingresar.setEnabled(puedeReingresarGratis);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarRActionPerformed

    private void btnReingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReingresarActionPerformed

        String placa = txtPlacaR.getText().trim();
    if (placa.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ingresa una placa.");
        return;
    }

/////////////////////// 1) Buscar la √öLTIMA salida registrada para esa placa
    proyectorparqueo.model.ReciboSalida r =
            proyectorparqueo.model.DatosApp.ultimaSalidaDe(placa);

    if (r == null) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "No hay historial de salida para esta placa.");
        return;
    }

    proyectorparqueo.model.vehiculo v = r.getVehiculo();

    // 2) Validar que el plan sea FLAT
    String plan = (v.getTipoPlan() == null) ? "" : v.getTipoPlan().toUpperCase();
    if (!plan.contains("FLAT")) {
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "El reingreso solo aplica para vehiculos con PLAN (FLAT)."
        );
        return;
    }

//////////////////////// 3) Validar que no hayan pasado m√°s de 2 horas desde la SALIDA
    java.time.LocalDateTime horaSalida = r.getHoraSalida();
    java.time.LocalDateTime ahora = java.time.LocalDateTime.now();
    long minutosFuera = java.time.Duration.between(horaSalida, ahora).toMinutes();

    if (minutosFuera > 120) {
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Han pasado mas de 2 horas desde la salida.\n"
              + "El plan FLAT se considera cancelado.\n"
              + "Debes registrarlo como un ingreso nuevo normal."
        );
        return;
    }

/////////////////////// 4) Validar cupo del √°rea donde estaba estacionado
    proyectorparqueo.model.Area area =
            proyectorparqueo.model.DatosApp.getAreaPorNombre(v.getArea());

    if (area != null && area.estaLlena()) {
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "No hay cupo en el area " + area.getNombre() + " para reingresar."
        );
        return;
    }

///////////////////////// 5) Crear NUEVO veh√≠culo con nueva hora de ingreso (ahora)
    proyectorparqueo.model.vehiculo nuevo = new proyectorparqueo.model.vehiculo(
                    v.getPlaca(),
                    v.getPropietario(),
                    v.getTipoVehiculo(),
                    v.getTipoPlan(),
                    true,          // planActivo
                    ahora,         // nueva hora de ingreso
                    v.getRol(),
                    v.getArea()
            );
    
/////////////////////////GUARDA EN MEMORIA
    DatosApp.PARQUEO.registrarVehiculo(nuevo);
    VehiculoDAO.insertar(nuevo); // GUARDARLO EN SQL
    JOptionPane.showMessageDialog(this, "REINGRESO REGISTRADO CORRECTAMENTE");

////////////////////////// 6) Registrar en el parqueo y actualizar ocupaci√≥n
   // proyectorparqueo.model.DatosApp.PARQUEO.registrarVehiculo(nuevo);
    if (area != null) {
        area.setOcupados(area.getOcupados() + 1);
    }

////////////////////// Importante: ya reingres√≥, quitarlo de PENDIENTES_FLAT
    proyectorparqueo.model.DatosApp.PENDIENTES_FLAT.remove(nuevo.getPlaca().toUpperCase());

///////////////////////// 7) Mostrar en el √°rea de texto
    java.time.format.DateTimeFormatter f =
            java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");

    txtAreaR.append(String.format(
            "%n%n‚úÖ Veh√≠culo reingresado:%n" +
            "Placa: %s%n" +
            "Propietario: %s%n" +
            "Plan: %s%n" +
            "Rol: %s | √Årea: %s%n" +
            "Hora reingreso: %s%n" +
            "Tiempo fuera: %d minutos",
            nuevo.getPlaca(),
            nuevo.getPropietario(),
            nuevo.getTipoPlan(),
            nuevo.getRol(),
            nuevo.getArea(),
            nuevo.getHoraIngreso().format(f),
            minutosFuera
    ));

    javax.swing.JOptionPane.showMessageDialog(this, "Reingreso registrado con √©xito.");
    btnReingresar.setEnabled(false);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnReingresarActionPerformed

